function single(zip, callback) {
    request({ url: url, method: 'POST', headers: headers, form: { zipCode: zip }, gzip: true },
        function (e, r, b) {
            if (e) {
                fs.appendFileSync('error.txt', e);
                callback();
                return;
            }

            if (!b) {
                callback();
                return;
            }

            var respString = b.toString().replace(/\\u003c/g, '<').replace(/\\u003e/g, '>');
            // fs.writeFileSync('resp.json', respString);
            try {
                var respJson = JSON.parse(respString);

                var html = respJson.returnData;
                // html = html.replace(/=\\/g, '=').replace(/\\"/g, '"');
                var $ = cheerio.load(html);
            } catch (err) {
                fs.appendFileSync('error.txt', err);
                callback();
                return;
            }
            $('.row-pairing').each(function (index, element) {
                var name = $(this).find('.firm-name-expand').text();
                var phone = $(this).find('.phone a').text();
                var email = $(this).find('.email a').attr('href').replace('mailto:', '');
                var website = $(this).find('a[data-tracking="firm_website"]').text();
                var location = $(this).find('a[data-tracking="firm_distance"]').text();
                var iard = $(this).find('.content-right .additional-firm-info').eq(0).text().replace('Firm IARD/CRD#: ', ''); /** === '' ? '' : $(this).find('.content-right .additional-firm-info').eq(0).text().match('\\d+')[0];*/
                var assets = $(this).find('.content-right .additional-firm-info').eq(1).text().replace('Minimum investable assets: ', ''); /**=== '' ? '' : $(this).find('.content-right .additional-firm-info').eq(1).text().match('\$\\d+')[0]; */
                // fs.appendFileSync('result.csv', [name, phone, email, website, location, iard, assets].join(',') + '\r\n');
                rows.push([name, phone, email, website, location, iard, assets]);
            });

            setTimeout(function () {
                console.log(zip + ' was done');
                callback();
            }, 3000);

        });
}



